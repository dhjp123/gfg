class Solution{
  public:
  TNode* bst(vector<int>&v,int l,int r){
      if(l>r){
          return nullptr;
      }
      int mid= (l+r+1)/2;
      TNode* curr= new TNode(v[mid]);
      curr->left=bst(v,l,mid-1);
      curr->right=bst(v,mid+1,r);
      return curr;
      
  }
    TNode* sortedListToBST(LNode *head) {
        //code here
        if(head ==nullptr){
            return nullptr;
        }
        int cnt=0;
        vector<int>v;
        while(head){
            v.push_back(head->data);
            head = head->next;
            cnt++;
        }
        
        int l =0;
        int r = cnt-1;
        
        return bst(v,l,r);
    }
};

