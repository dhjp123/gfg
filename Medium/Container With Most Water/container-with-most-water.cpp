long long maxArea(long long height[], int len)
{
    // Your code goes here
    int water = 0;
    int i = 0, j = len - 1;
    while (i < j) {
        int h = min(height[i], height[j]);
        water = max(water, (j - i) * h);
        while (height[i] <= h && i < j) i++;
        while (height[j] <= h && i < j) j--;
    }
    return water;
}

